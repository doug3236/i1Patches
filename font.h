#include "array2d.h"

using RGB = std::array<float, 3>;

class Font {
    static short font[];
    Array2D<RGB> fonts;
public:
    Font() : fonts{ 512,512 } {
        int i = 0;
        float current = 255.0;
        for (int index=0;;index++)
        {
            int count = font[index];
            if (count == 0)
                break;
            for (; count > 0; count--)
            {
                fonts(i % 512, i / 512) = RGB{ current,current,current };
                i++;
            }
            current = current == 0 ? 255.0f : 0;
        }
        if (i != 512*512)
            throw "oops";
        // Code to verify RLL encode/decode is correct
        //void TiffWrite(const char* file, const Array2D<RGB> rgb);
        //TiffWrite("test3.tif", fonts);
    }
    inline Array2D<RGB> get_char(char c, int factor)
    {
        //Array2D<RGB>{50, 30};
        Array2D<RGB> c_image = fonts.extract(((c - 32) / 16)*64, 50, ((c - 32) % 16)*32, 30);
        Array2D<RGB> ret(factor * 50, factor * 30);
        for (int row = 0; row < c_image.nr; row++)
            for (int col = 0; col < c_image.nc; col++)
                for (int i = factor * row; i < factor * row + factor; i++)
                    for (int ii = factor * col; ii < factor * col + factor; ii++)
                        ret(i, ii) = c_image(row, col);
        return ret;
    }
    inline Array2D<RGB> get_line(const std::string& line, int factor = 2, float text  = 0, float background = 255)
    {
        Array2D<RGB> ret{factor*50, int(factor*30*line.size())};
        for (int i = 0; i < int(line.size()); i++)
        {
            Array2D<RGB> v = get_char(line[i], factor);
            for (auto& x : v.v)
                for (auto& y : x)
                    y = y == 0.0f ? text : background;
            ret.insert(v, 0, i * factor * 30);
        }
        return ret;
    }
};

